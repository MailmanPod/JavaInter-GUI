    private void btnDispatchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDispatchActionPerformed
        List<Order> los = new LinkedList<Order>();
        
        los.add(order);
        
        OrderFacadeSubject.getInstance().dispathOrders(los);
        
        initFields();
        
        this.btnDelivery.setEnabled(true);
        this.btnCancel.setEnabled(true);
        this.btnDispatch.setEnabled(false);
    }//GEN-LAST:event_btnDispatchActionPerformed

    private void btnDeliveryActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeliveryActionPerformed
        List<Order> los = new LinkedList<>();
        los.add(order);
        
        OrderFacadeSubject.getInstance().deliveryOrders(los);
        initFields();
        
        this.btnDelivery.setEnabled(false);
    }//GEN-LAST:event_btnDeliveryActionPerformed

    private void btnCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelActionPerformed
        List<Order> los = new LinkedList<>();
        los.add(order);
        
        OrderFacadeSubject.getInstance().cancelOrder(los);
        initFields();
        btnDelivery.setEnabled(false);
        btnDispatch.setEnabled(false);
        btnCancel.setEnabled(false);
    }//GEN-LAST:event_btnCancelActionPerformed

