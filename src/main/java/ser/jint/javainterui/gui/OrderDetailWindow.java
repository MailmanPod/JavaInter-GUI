    private void centrarPantalla() {
        Dimension tamFrame = this.getSize();//para obtener las dimensiones del frame
        Dimension tamPantalla = Toolkit.getDefaultToolkit().getScreenSize();//para obtener el tama√±o de la pantalla
        setLocation((tamPantalla.width - tamFrame.width) / 2, (tamPantalla.height - tamFrame.height) / 2);//para posicionar
    }
    
    private void initFields(){
        this.txtClientDNI.setText(new Integer(order.getClientIdentificationNumber()).toString());
        this.txtOrderAddress.setText(this.order.getOrderAddress());
        this.txtTotalAmount.setText(new Double(this.order.getOrderTotal()).toString());
        this.txtOrderState.setText(this.order.getContextState());
        this.txtCPA.setText(this.order.getOrderZipAddress());
        OrderDetailDataModel model = new OrderDetailDataModel(order.getOrderDetails());
        
        this.tblOrderContent.setModel(model);
        
        if(order.isDelivered() || order.isCanceled()){
            btnDispatch.setEnabled(false);
            btnDelivery.setEnabled(false);
            btnCancel.setEnabled(false);
        }else if(order.getContextState().compareToIgnoreCase(State.DISPATCH_STATE) == 0){
            btnDispatch.setEnabled(false);
            btnDelivery.setEnabled(true);
            btnCancel.setEnabled(true);
        }
    }
    
